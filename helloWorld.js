

function cekGenapGanjil(angka) {
    if (angka % 2 === 0) {
        return "Genap";
    } else {
        return "Ganjil";
    }
}

function besarKecil(jumlah){
    if (jumlah > 10 ){
        return "kecil"
    } else {
        return "besar"
    }
}

// Contoh penggunaan:
console.log(cekGenapGanjil(5));  // Output: "Genap"
